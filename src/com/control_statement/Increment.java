package com.control_statement;

public class Increment {

	public static void main(String[] args) {

		int a = 557;

		System.out.println(a++);
		
		System.out.println(++a);
		
		System.out.println(a++);
	
		System.out.println(++a);
		
		System.out.println(++a);
		
		System.out.println(++a + ++a);
		
	}
	

}
