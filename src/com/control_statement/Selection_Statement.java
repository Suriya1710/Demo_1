package com.control_statement;

public class Selection_Statement {

	public static void main(String[] args) {

		int a = 5;

		switch (a) {
		case 4:

			System.out.println("Four");

			break;

		case 5:
			System.out.println("Five");

		case 6:

			System.out.println("Six");

			break;

		case 7:
			System.out.println("Seven");

		default:
			break;
		}
		
		

	}

}
